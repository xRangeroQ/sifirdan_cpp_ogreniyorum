// Module(s)
#include <iostream>

// Used Namespace(s)
using namespace std;

// Main Block
int main() {

    // Age declare
    int age = 19;

    // If Statement
    if (age > 18)
        cout << "You are older than 18 years old.";

    // Else If Statement
    else if (age == 18)
        cout << "You are 18 years old.";

    // Else Statement
    else
        cout << "You are younger than 18 years old.";

    // Return Program
    return 0;
}